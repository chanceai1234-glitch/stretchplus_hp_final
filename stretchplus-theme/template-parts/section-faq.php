        <h2>よくあるご質問</h2>
        <p class="faq-lead">初めての方からよくいただくご質問をまとめました。<br>準備・予約・施術・プランごとにご覧いただけます。</p>

        <div class="faq-grid">
            <!-- 準備 -->
            <div class="faq-category">
                <h3>準備</h3>
                <div class="faq-accordion">
                    <details>
                        <summary>必要な持ち物や、服装の指定はありますか？</summary>
                        <p>特にございません。無料のお着換えやウォーターサーバーをご用意しておりますので、お気軽にご来店ください。<br>※
                            サブスクリプションプランでご登録いただく場合、デビットカード/クレジットカード情報が必要となりますので、ご用意ください。</p>
                    </details>
                    <details>
                        <summary>駐車場はありますか？</summary>
                        <p>徒歩1~2分程度の場所に、2か所ほどコインパーキングがございます。また、お店の前に、2台分のパーキングメーター（60分まで）もございます。</p>
                    </details>
                    <details>
                        <summary>おすすめの来店頻度はありますか？</summary>
                    <p>スタッフがお客様の健康状態や体の制約を考慮して、最適な来店頻度をご案内いたします。</p>
                </details>
                <details>
                    <summary>性別や年齢による制限はありますか？</summary>
                    <p>特に制限はありません。様々な層のお客様に対して、経験豊富なスタッフが最適なストレッチをご提案いたします。</p>
                </details>
                <details>
                    <summary>ケガがある場合でも受けられますか？</summary>
                    <p>症状に依りますので、主治医とご相談のうえ、ご来店ください。</p>
                </details>
                <details>
                    <summary>妊娠している場合でも受けられますか？</summary>
                    <p>妊娠している場合は施術を受けられません。ご了承ください。</p>
                </details>
            </div>
        </div>

        <!-- 予約 -->
        <div class="faq-category">
            <h3>予約</h3>
            <div class="faq-accordion">
                <details>
                    <summary>どこから予約できますか？</summary>
                    <p>ページ右上の「WEBで予約する」ボタンからご予約が可能です。そのほかにもお電話、公式LINEからのご予約、HOT PEPPER Beauty、Rakuten Beauty、minimoよりご予約いただけます。</p>
                </details>
                <details>
                    <summary>スタッフは指名できますか?</summary>
                    <p>指名可能です。指名出来るスタッフは、出勤状況や予約状況によって変わりますので、ご予約の際にご確認ください。</p>
                </details>
                <details>
                    <summary>当日予約は可能ですか？</summary>
                    <p>当日予約は可能ですが、ご希望のお時間で予約出来ない場合がございますので、早めのご予約をおすすめしております。</p>
                </details>
                <details>
                    <summary>予約内容のキャンセルや変更は可能ですか？</summary>
                    <p>可能です。予約内容のキャンセルや変更は、出来る限り前日の営業時間内までにお願いいたします。</p>
                </details>
            </div>
        </div>

        <!-- 施術 -->
        <div class="faq-category">
            <h3>施術</h3>
            <div class="faq-accordion">
                <details>
                    <summary>運動経験がないのですが大丈夫でしょうか？</summary>
                    <p>問題ありません。スポーツのパフォーマンス向上のために当店に通っていただいているお客様も多数いらっしゃいますが、ほとんどのお客様が肩や腰など、ご自身の体の一部に違和感や不安を持っておられるお客様です。
                    </p>
                </details>
                <details>
                    <summary>一般的なストレッチと何が違うんですか？</summary>
                    <p>一般的なストレッチでは同じ動作を繰り返すことが多いですが、パーソナルストレッチではプロのスタッフがお客様ごとにストレッチをカスタマイズし、最適な効果を追求します。</p>
                </details>
                <details>
                    <summary>パーソナルストレッチは、どのような人におすすめですか？</summary>
                    <p>体の柔軟性を向上させたい方、筋肉の緊張を和らげたい方、姿勢を改善したい方など、健康とストレッチに関心のあるすべての方におすすめです。また、特定の運動目標を持つ方にも効果的です。</p>
                </details>
            </div>
        </div>

        <!-- プラン -->
        <div class="faq-category">
            <h3>プラン</h3>
            <div class="faq-accordion">
                <details>
                    <summary>入会手続に必要なものはありますか？</summary>
                    <p>会員制では無いため、入会手続や退会手続はございません。ただし、サブスクリプションプランに加入いただく場合は、デビット/クレジットカード情報が必要です。</p>
                </details>
                <details>
                    <summary>初めての場合はどのプランを予約すれば良いですか？</summary>
                    <p>60分以上のコースをおすすめしております。初回のストレッチでは、関節の動きなどをしっかりと確認したうえで、最適なコースをご提案させていただきます。</p>
                </details>
                <details>
                    <summary>法人用プランや出張プランはありますか？</summary>
                    <p>ご要望に合わせて柔軟に対応いたします。お電話か公式LINEよりご相談ください。</p>
                </details>
            </div>
